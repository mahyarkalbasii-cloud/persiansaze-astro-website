function createPersianSlug(value: string) {
  return value
    .normalize("NFKC")
    .replace(/[\u064A]/g, "\u06CC")
    .replace(/[\u0643]/g, "\u06A9")
    .replace(/[\u0640]/g, "")
    .toLowerCase()
    .replace(/[^\p{L}\p{N}]+/gu, "-")
    .replace(/^-+|-+$/g, "");
}

